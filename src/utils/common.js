export function animalArr() {
  let animal = ['鼠', '牛', '虎', '兔', '龙', '蛇', '马', '羊', '猴', '鸡', '狗', '猪'];
  let currentAnimal = _calcYearToAnimal();
  let index = animal.indexOf(currentAnimal);
  // 中文文字
  let textLeftAnimal = animal.slice(0, index + 1);
  let textRightAnimal = animal.slice(index + 1, animal.length);
  let textAnimal = [].concat(textRightAnimal).concat(textLeftAnimal).reverse();
  let animalArr = [[], [], [], [], [], [], [], [], [], [], [], []];
  for (let i = 0; i < 49; i++) {
    let num = i < 9 ? '0' + (i + 1) : String(i + 1);
    let e = i % 12;
    animalArr[e].push(num);
  }
  let newArr = [];
  for (let i = 0; i < animalArr.length; i++) {
    let obj = {
      animal: textAnimal[i],
      arr: animalArr[i]
    };
    newArr.push(obj)
  }
  return newArr;
}

export function animal(number, type, _this) {
  // type---> class, text
  let animal = ['鼠', '牛', '虎', '兔', '龙', '蛇', '马', '羊', '猴', '鸡', '狗', '猪'];
  let en_animal = ['Rat', 'Cow', 'Tiger', 'Rabbit', 'Dragon', 'Snake', 'Horse', 'Sheep', 'Monkey', 'Chicken', 'Dog', 'Pig'];
  let arr = ['shu', 'niu', 'hu', 'tu', 'long', 'she', 'ma', 'yang', 'hou', 'ji', 'gou', 'zhu'];
  let currentAnimal = _calcYearToAnimal();
  let index = animal.indexOf(currentAnimal);
  // 字母
  let leftAnimal = arr.slice(0, index + 1);
  let rightAnimal = arr.slice(index + 1, arr.length);
  // 中文文字
  let textLeftAnimal = animal.slice(0, index + 1);
  let textRightAnimal = animal.slice(index + 1, arr.length);
  // 英语文字
  let enLeftAnimal = en_animal.slice(0, index + 1);
  let enRightAnimal = en_animal.slice(index + 1, arr.length);

  let newAnimal = [].concat(rightAnimal).concat(leftAnimal).reverse();
  let textAnimal = [].concat(textRightAnimal).concat(textLeftAnimal).reverse();
  let englishAnimal = [].concat(enRightAnimal).concat(enLeftAnimal).reverse();

  let n = 0;
  if (Number(number.slice(0, 1)) === 0) {
    n = Number(number.slice(1, 2))
  } else {
    n = Number(number)
  }
  let currentIndex = (n - 1) % 12;
  // console.log(n, newAnimal[currentIndex], newAnimal, index, 'aaaaaa');
  // console.log(textAnimal[currentIndex], 'abbb');
  if (type === 'text') {
    if (_this.$i18n.locale === LANG.china) {
      return textAnimal[currentIndex]
    } else {
      return englishAnimal[currentIndex]
    }
  } else {
    return newAnimal[currentIndex]
  }
};

function _calcYearToAnimal() {
  let num = (new Date().getFullYear() - 1900) % 12;//1900 是鼠
  // let num = (2021 - 1900) % 12;
  let result = '';
  switch (num) {
    case 0:
      result = '鼠';
      break;
    case 1:
      result = '牛';
      break;
    case 2:
      result = '虎';
      break;
    case 3:
      result = '兔';
      break;
    case 4:
      result = '龙';
      break;
    case 5:
      result = '蛇';
      break;
    case 6:
      result = '马';
      break;
    case 7:
      result = '羊';
      break;
    case 8:
      result = '猴';
      break;
    case 9:
      result = '鸡';
      break;
    case 10:
      result = '狗';
      break;
    case 11:
      result = '猪';
      break;
  }
  return result;
}

export function formatTime(seconds) {
  let s = 1, m = 60 * s, h = m * 60;
  let ss = 0, mm = 0, hh = 0;
  if (s > 0) {
    hh = Math.floor(seconds / h);
    mm = Math.floor(seconds % h / m);
    ss = Math.floor(seconds % h % m / s);
  }
  let p = function (t) {
    return t < 10 ? '0' + t : t;
  };
  return [p(hh), p(mm), p(ss)];
}

