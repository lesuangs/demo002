export function _compare(property) {
  return function (a, b) {
    let value1 = a[property];
    let value2 = b[property];
    return value1 - value2;
  }
}

export function isPair(list) {
  let arr = list;
  let result = '';
  if (arr[0] === arr[1] && arr[0] === arr[2] && arr[1] === arr[2]) {
    result = i18n.t('open.豹子');
  } else {
    let num = 0;
    if (arr[0] === arr[1]) {
      num++;
    }
    if (arr[0] === arr[2]) {
      num++;
    }
    if (arr[1] === arr[2]) {
      num++;
    }
    if (num === 1) {
      result = i18n.t('open.对子');
    } else {
      let n = 0;
      if (Math.abs(arr[0] - arr[1]) === 1) {
        n++;
      }
      if (Math.abs(arr[0] - arr[2]) === 1) {
        n++;
      }
      if (Math.abs(arr[1] - arr[2]) === 1) {
        n++;
      }
      if (n === 0) {
        result = i18n.t('open.杂六');
      }
      if (n === 1) {
        result = i18n.t('open.半顺');
      }
      if (n === 2) {
        result = i18n.t('open.顺子');
      }
    }
  }
  return result;
}

